function infoConfirm(){
	if(document.join_frame.id.value.lengh == 0){
		alert("please enter id");
		join_frame.id.focus();
		return;
	}
	
	if(document.join_frame.pw.value.lengh == 0){
		alert("please enter pw");
		join_frame.pw.focus();
		return;
	}
	
	if(document.join_frame.pw.value != document.join_frame.pw_confirm.value){
		alert("wrong pw");
		join_frame.pw_confirm.focus();
		return;
	}
	
	if(document.join_frame.name.value.lengh == 0){
		alert("please enter name");
		join_frame.name.focus();
		return;
	}
	
	if(document.join_frame.phone.value.lengh == 0){
		alert("please enter phone number");
		join_frame.phone.focus();
		return;
	}
	
	document.join_frame.submit();
}